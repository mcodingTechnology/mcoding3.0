package com.mcoding.emis.goods.company.bean;

public class CompanyApiAdressResp {

	private String province;

	private String city;

	private String county;

	private String address;

	private String officeAddr;

	private String receiverName;

	private String receiverMoblie;

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getCounty() {
		return county;
	}

	public void setCounty(String county) {
		this.county = county;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getReceiverName() {
		return receiverName;
	}

	public void setReceiverName(String receiverName) {
		this.receiverName = receiverName;
	}

	public String getReceiverMoblie() {
		return receiverMoblie;
	}

	public void setReceiverMoblie(String receiverMoblie) {
		this.receiverMoblie = receiverMoblie;
	}

	public String getOfficeAddr() {
		return officeAddr;
	}

	public void setOfficeAddr(String officeAddr) {
		this.officeAddr = officeAddr;
	}

}
